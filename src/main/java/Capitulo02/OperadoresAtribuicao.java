package Capitulo02;

public class OperadoresAtribuicao {
    public static void main(String[] args) {

        int w = 10;
        w = w + 15;

        System.out.println(w);

        int z = 10;
        z += 15;

        System.out.println(z);

        z *= 10;
        System.out.println(z);

        z -=5;
        System.out.println(z);

        z /= 2;
        System.out.println(z);

        z %= 3;

        System.out.println(z);

    }
}
